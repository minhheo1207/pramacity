// Helper functions
export function getProvinces() {
  return Object.keys(vietnamLocations).sort();
}

export function getDistrictsByProvince(province) {
  if (!province || !vietnamLocations[province]) return [];
  return Object.keys(vietnamLocations[province]).sort();
}

export function getWardsByProvinceAndDistrict(province, district) {
  if (!province || !district || !vietnamLocations[province] || !vietnamLocations[province][district]) {
    return [];
  }
  return vietnamLocations[province][district].sort();
}

